let Language = Number(prompt("Enter a number between 1 to 5: "));
switch (Language) {
  case 1:
    alert("You chose English !");
    break;

  case 2:
    alert("You chose Spanish !");
    break;

  case 3:
    alert("You chose Hindi !");
    break;

  case 4:
    alert("You chose Arabic !");
    break;

  case 5:
    alert("You chose Gujarati !");
    break;
  default:
    alert("WRONG INPUT");
}